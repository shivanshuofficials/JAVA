package Bit_manipulation;

public class binary_left_shift {
    public static void main(String[] args) {
        // Left shift
        // 5 -> 00000101
        // 5 << 1 -> 1010 -> 10
        // 5 << 2 -> 10100 -> 20
        // 5 << 3 -> 101000 -> 40

        System.out.println(5 << 1);
        System.out.println(5 << 2);
        System.out.println(5 << 3);
    }
}
