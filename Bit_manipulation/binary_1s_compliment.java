package Bit_manipulation;

public class binary_1s_compliment {
    public static void main(String[] args) {
        // 1's compliment
        // 0 -> 1
        // 1 -> 0

        System.out.println(~5);

        // 5 -> 101
        // 1's compliment of 5 -> 010
        // 101 + 1 -> 110
        // 110 -> -6
    }   
}
