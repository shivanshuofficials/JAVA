package Bit_manipulation;

public class binary_right_shift {
    public static void main(String[] args) {
        // Right shift
        // 5 -> 00000101
        // 5 >> 1 -> 00000010 -> 2
        // 5 >> 2 -> 00000001 -> 1
        // 5 >> 3 -> 00000000 -> 0

        System.out.println(5 >> 1);
        System.out.println(5 >> 2);
        System.out.println(5 >> 3);
    }
    
}
