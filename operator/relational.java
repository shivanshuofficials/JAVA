package operator;

public class relational {
    public static void main(String[] args) {
        //equal to equal to operator
        int a = 10;
        int b = 20;
        int c = 10;
        System.out.println((a != b));//not equal
        System.out.println((a == c));//double equal
        System.out.println((a >= c));//greater or equals to
        System.out.println((a <= c));//less or equals to
        System.out.println((a < c));//less then
        System.out.println((a > c));//graeter then
    }
}
