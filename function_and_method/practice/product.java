package function_and_method.practice;

public class product {
    public static void products(int a, int b) {
        int product = a * b;
        System.out.println(product);
    
        return;
    }
    public static void main(String[] args) {
        int a = 10;
        int b = 19;
    
        
        products(a, b);
      
    }
}
